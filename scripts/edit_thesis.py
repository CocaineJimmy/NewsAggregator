def set_text(p, text):
    """Replace paragraph text while keeping the formatting of its first run."""
    # Remove all runs except the first
    runs = p.runs
    if not runs:
        # Empty paragraph: add a run
        p.add_run(text)
        return
    first = runs[0]
    # Remove other runs
    for r in runs[1:]:
        r._element.getparent().remove(r._element)
    # Set text on first run
    first.text = text


def insert_paragraph_after(template_p, text):
    """Insert a new paragraph (clone of template_p) right after template_p with given text.
    Returns the new Paragraph object (as raw element-wrapped equivalent)."""
    new_el = deepcopy(template_p._element)
    template_p._element.addnext(new_el)
    # Wrap as Paragraph for convenience
    from docx.text.paragraph import Paragraph
    new_p = Paragraph(new_el, template_p._parent)
    set_text(new_p, text)
    return new_p


def insert_paragraphs_after(template_p, texts):
    """Insert multiple paragraphs (each cloning template_p) after template_p in order."""
    last = template_p
    new_paras = []
    for t in texts:
        last = insert_paragraph_after(last, t)
        new_paras.append(last)
    return new_paras


def find_paragraph_by_text(doc, contains, start=0):
    for i, p in enumerate(doc.paragraphs):
        if i < start:
            continue
        if contains in p.text:
            return i, p
    return -1, None


